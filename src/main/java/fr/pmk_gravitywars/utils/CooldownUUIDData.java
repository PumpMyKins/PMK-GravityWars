package fr.pmk_gravitywars.utils;

import java.util.UUID;

public class CooldownUUIDData {

	private UUID normalTNT;
	private UUID poissonTNT;
	private UUID levitateTNT;
	private UUID blindTNT;
	
	public UUID getNormalTNT() {
		return normalTNT;
	}
	public void setNormalTNT(UUID normalTNT) {
		this.normalTNT = normalTNT;
	}
	public UUID getPoissonTNT() {
		return poissonTNT;
	}
	public void setPoissonTNT(UUID poissonTNT) {
		this.poissonTNT = poissonTNT;
	}
	public UUID getLevitateTNT() {
		return levitateTNT;
	}
	public void setLevitateTNT(UUID levitateTNT) {
		this.levitateTNT = levitateTNT;
	}
	public UUID getBlindTNT() {
		return blindTNT;
	}
	public void setBlindTNT(UUID blindTNT) {
		this.blindTNT = blindTNT;
	}
	
}
